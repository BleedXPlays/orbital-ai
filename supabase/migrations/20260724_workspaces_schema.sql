  add column if not exists selected_project text default '',
  add column if not exists archived_chats jsonb default '[]'::jsonb,
  add column if not exists archived_projects jsonb default '[]'::jsonb,
  add column if not exists pinned_chats jsonb default '[]'::jsonb,
  add column if not exists chat_activity jsonb default '{}'::jsonb,
  add column if not exists activity_log jsonb default '[]'::jsonb,
  add column if not exists created_at timestamptz default now(),
