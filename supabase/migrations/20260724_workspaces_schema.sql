update public.workspaces
set
  name = coalesce(name, 'My Workspace'),
  chats = coalesce(chats, '[]'::jsonb),
  chat_messages = coalesce(chat_messages, '{}'::jsonb),
  projects = coalesce(projects, '[]'::jsonb),
  project_chats = coalesce(project_chats, '{}'::jsonb),
  project_files = coalesce(project_files, '{}'::jsonb),
  project_notes = coalesce(project_notes, '{}'::jsonb),
  selected_chat = coalesce(selected_chat, ''),
  selected_project = coalesce(selected_project, ''),
  archived_chats = coalesce(archived_chats, '[]'::jsonb),
  archived_projects = coalesce(archived_projects, '[]'::jsonb),
  pinned_chats = coalesce(pinned_chats, '[]'::jsonb),
  chat_activity = coalesce(chat_activity, '{}'::jsonb),
  activity_log = coalesce(activity_log, '[]'::jsonb),
  created_at = coalesce(created_at, now()),
  updated_at = coalesce(updated_at, now());

-- Keep the newest row if an older setup accidentally created duplicate
-- workspaces for the same Firebase user.
delete from public.workspaces older
using public.workspaces newer
where older.user_id = newer.user_id
  and (
    older.updated_at < newer.updated_at
    or (
      older.updated_at = newer.updated_at
      and older.ctid < newer.ctid
    )
  );

create unique index if not exists workspaces_user_id_unique_idx
  on public.workspaces (user_id);

create unique index if not exists workspaces_id_unique_idx
  on public.workspaces (id);

