-- Complete persistence schema for the OrbitalAI workspace.
-- This migration is safe to run after an older, partial workspaces table.

create table if not exists public.workspaces (
  id bigint generated by default as identity primary key,
  user_id text not null,
  name text not null default 'My Workspace',
  chats jsonb not null default '[]'::jsonb,
  chat_messages jsonb not null default '{}'::jsonb,
  projects jsonb not null default '[]'::jsonb,
  project_chats jsonb not null default '{}'::jsonb,
  project_files jsonb not null default '{}'::jsonb,
  project_notes jsonb not null default '{}'::jsonb,
  selected_chat text not null default '',
  selected_project text not null default '',
  archived_chats jsonb not null default '[]'::jsonb,
  archived_projects jsonb not null default '[]'::jsonb,
  pinned_chats jsonb not null default '[]'::jsonb,
  chat_activity jsonb not null default '{}'::jsonb,
  activity_log jsonb not null default '[]'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.workspaces
  add column if not exists id bigint generated by default as identity,
  add column if not exists user_id text,
  add column if not exists name text default 'My Workspace',
  add column if not exists chats jsonb default '[]'::jsonb,
  add column if not exists chat_messages jsonb default '{}'::jsonb,
  add column if not exists projects jsonb default '[]'::jsonb,
  add column if not exists project_chats jsonb default '{}'::jsonb,
  add column if not exists project_files jsonb default '{}'::jsonb,
  add column if not exists project_notes jsonb default '{}'::jsonb,
