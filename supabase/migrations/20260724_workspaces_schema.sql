  to anon, authenticated;

do $$
declare
  workspace_sequence text;
begin
  workspace_sequence := pg_get_serial_sequence('public.workspaces', 'id');

  if workspace_sequence is not null then
    execute format(
      'grant usage, select on sequence %s to anon, authenticated',
      workspace_sequence
    );
  end if;
end;
$$;

drop policy if exists "Users manage only their workspace"
  on public.workspaces;
create policy "Users manage only their workspace"
  on public.workspaces
  for all
  to anon, authenticated
  using (
    user_id = (select auth.jwt() ->> 'sub')
  )
  with check (
    user_id = (select auth.jwt() ->> 'sub')
  );

